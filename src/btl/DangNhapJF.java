    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String loai = taikhoan();
        if(loai.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Tài khoản không tồn tại");
        }else if(loai.equals("ad")){
            mauser = txtuser.getText();
            loaiuser = loai;
            new Admin().setVisible(true);
            this.dispose();
        }else if(loai.equals("hocsinh")){
            mauser = txtuser.getText();
            loaiuser = "hocsinh";
            new HocSinhForm().setVisible(true);
            this.dispose();
        }else if(loai.equals("chủ nhiệm")){
            mauser = txtuser.getText();
            loaiuser = "chủ nhiệm";
            new GiaoVienForm().setVisible(true);
            this.dispose();
        }else if(loai.equals("bộ môn")){
            mauser = txtuser.getText();
            loaiuser = "bộ môn";
            new Diem().setVisible(true);
            this.dispose();
        }else if(loai.equals("totruong")){
            mauser = txtuser.getText();
            loaiuser = "totruong";
            new ToTruongForm().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

