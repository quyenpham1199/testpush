    private String taikhoan(){
        String user=txtuser.getText();
        String pass = new String(txtpass.getPassword());
        danhsach = new DangNhapCon().layDanhSachtaikhoan("select * from dangnhap");
        String loai ="";
        for(DangNhap d : danhsach){
            if(user.equals(d.getUser())){
                loai=d.getLoaitaikhoan();
            }
        }
        return loai;
    }
