    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // thêm
        String maLop=null, tenLop= null, maGV=null;
        int siso=0;
        boolean ok= true;
        if("".equals(txtmaLop.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma lop khong duoc de trong");
            ok= false;
        }
        else {
            maLop= txtmaLop.getText();
        }
        if("".equals(txttenLop.getText())){
            JOptionPane.showMessageDialog(rootPane, "Tên lop khong duoc de trong");
            ok= false;
        }
        else {
           tenLop= txttenLop.getText();
        }
        if("".equals(txtmaGV.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma giao vien khong duoc de trong");
            ok= false;
        }
        else {
            maGV= txtmaGV.getText();
        }
        try {
            siso = Integer.parseInt(txtsiso.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Sĩ số không hợp lệ");
            ok = false;
        }
        if(ok){
            DoiTuong.Lop h= new DoiTuong.Lop(maLop, tenLop, maGV, siso);
            new LopCon().ThemMoi(h);
            JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
            danhsach1.add(h);
            hienThiDanhSach("select* from lop");
            XoaText();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

