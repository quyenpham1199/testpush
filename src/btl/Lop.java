     public int hienThiDanhSach(String sql){
        danhsach1= new LopCon().layDanhSach1(sql);        
        model.setRowCount(0);
        for(DoiTuong.Lop h: danhsach1){
            model.addRow(new Object[]{
                h.getMaLop(), h.getTenLop(),h.getMaGV(),h.getSiso()
            }
                    );
        }
        return danhsach1.size();        
    }
     
