    public int hienThiDanhSach(String sql){
        danhsach1= new ToTruongCon().layDanhSach1(sql);        
        model.setRowCount(0);
        for(DoiTuong.ToTruong h: danhsach1){
            model.addRow(new Object[]{
                h.getMaGV(),h.getMaLop(), h.getChucvu(),
                 h.getMaM(), h.getMaPC()
            }
                    );
        }
        return danhsach1.size();        
    }
