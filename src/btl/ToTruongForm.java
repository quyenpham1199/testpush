    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //phân công
        String maGV=null, maLop= null, chucvu=null, maM=null, maPC=null;
        
        boolean ok= true;
        if("".equals(txtmaGV.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma giao vien khong duoc de trong");
            ok= false;
        }
        else {
            maGV= txtmaGV.getText();
        }
         if("".equals(txtmaLop.getText())){
            JOptionPane.showMessageDialog(rootPane, "Mã lớp không duoc de trong");
            ok= false;
        }
        else {
            maLop= txtmaLop.getText();
        }
         if("".equals(txtchucvu.getText())){
            JOptionPane.showMessageDialog(rootPane, "Chức vụ khong duoc de trong");
            ok= false;
        }
        else {
            chucvu= txtchucvu.getText();
        }
         if("".equals(txtmaM.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma mon khong duoc de trong");
            ok= false;
        }
        else {
            maM= txtmaM.getText();
        }
         if("".equals(txtmaPC.getText())){
            JOptionPane.showMessageDialog(rootPane, "Mã phân công khong duoc de trong");
            ok= false;
        }
        else {
            maPC= txtmaPC.getText();
        }
        if(ok){
            DoiTuong.ToTruong h= new DoiTuong.ToTruong(maGV,maLop, chucvu,maM,maPC);
            new ToTruongCon().ThemMoi(h);
            JOptionPane.showMessageDialog(rootPane, "Phân công thành công");
            danhsach1.add(h);
            hienThiDanhSach("select* from phancong");
            XoaText();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

