    public int hienThiDanhSach(String sql){
        danhsach1= new HocKyCon().layDanhSach1(sql);        
        model.setRowCount(0);
        for(DoiTuong.HocKy h: danhsach1){
            model.addRow(new Object[]{
               h.getHocky(),h.getMaHS(), 
                 h.getRenluyen(),h.getKhenthuong(),
                 h.getTotnghiep(), h.getNamhoc()
            }
                    );
        }
        return danhsach1.size();        
    }
    
