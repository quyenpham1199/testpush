    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //them
        String hocky=null, maHS= "", renluyen=null, khenthuong=null, totnghiep=null; 
        int namhoc=0;
        boolean ok= true;
        if("".equals(txthocky.getText())){
            JOptionPane.showMessageDialog(rootPane, "Hoc ky khong duoc de trong");
            ok= false;
        }
        else {
            hocky= txthocky.getText();
        }
        if("".equals(txtmaHS.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma hoc sinh khong duoc de trong");
            ok= false;
        }
        else {
            maHS= txtmaHS.getText();
        }
         if("".equals(txtrenluyen.getText())){
            JOptionPane.showMessageDialog(rootPane, "Rèn luyện không được để trống");
            ok= false;
        }
        else {
            renluyen= txtrenluyen.getText();
        }
         if("".equals(txtkhenthuong.getText())){
            JOptionPane.showMessageDialog(rootPane, "Khen thuỏng không được để trống");
            ok= false;
        }
        else {
            khenthuong= txtkhenthuong.getText();
        }
         if("".equals(txttotnghiep.getText())){
            JOptionPane.showMessageDialog(rootPane, "Tốt nghiệp không được để trống");
            ok= false;
        }
        else {
            totnghiep= txttotnghiep.getText();
        }
         try {
            namhoc = Integer.parseInt(txtnamhoc.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Năm học không hợp lệ");
            ok = false;
        }
        if(ok){
            DoiTuong.HocKy h= new DoiTuong.HocKy(hocky,maHS,renluyen, khenthuong, totnghiep, namhoc);
            new HocKyCon().ThemMoi(h);
            JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
            danhsach1.add(h);
            hienThiDanhSach("select* from hocky");
            XoaText();
        }
    }                                        

