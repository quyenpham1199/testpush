    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //them
        String maM=null, tenM= null;
        
        boolean ok= true;
        if("".equals(txtmaM.getText())){
            JOptionPane.showMessageDialog(rootPane, "Ma mon khong duoc de trong");
            ok= false;
        }
        else {
            maM= txtmaM.getText();
        }
        if("".equals(txttenM.getText())){
            JOptionPane.showMessageDialog(rootPane, "Tên môn khong duoc de trong");
            ok= false;
        }
        else {
           tenM= txttenM.getText();
        }
        
        if(ok){
            DoiTuong.Mon h= new DoiTuong.Mon(maM, tenM);
            new MonCon().ThemMoi(h);
            JOptionPane.showMessageDialog(rootPane, "Them moi thanh cong");
            danhsach1.add(h);
            hienThiDanhSach("select* from mon");
            XoaText();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

