    public int hienThiDanhSach(String sql){
        danhsach1= new MonCon().layDanhSach1(sql);        
        model.setRowCount(0);
        for(DoiTuong.Mon h: danhsach1){
            model.addRow(new Object[]{
                h.getMaM(), h.getTenM()
            }
                    );
        }
        return danhsach1.size();        
    }
    
