public class Mon {
    private String maM;
    private String tenM;

    public String getMaM() {
        return maM;
    }

    public void setMaM(String maM) {
        this.maM = maM;
    }

    public String getTenM() {
        return tenM;
    }

    public void setTenM(String tenM) {
        this.tenM = tenM;
    }

    public Mon(String maM, String tenM) {
        this.maM = maM;
        this.tenM = tenM;
    }
    

    public Mon() {
    }

   
    
}
